// Método para leer el archivo <departamento>_vehiculos.txt y cargar los datos en la JTable
    private void leerArchivoVehiculos(String nombreCarpeta) {
        String rutaArchivo = rutaCarpetaPrincipal + "/" + nombreCarpeta + "/" + nombreCarpeta + "_vehiculos.txt";
        File archivo = new File(rutaArchivo);
        if (!archivo.exists()) {
            JOptionPane.showMessageDialog(this, "El archivo " + nombreCarpeta + "_vehiculos.txt no existe en la carpeta: " + nombreCarpeta);
            return;
        }

        try (BufferedReader reader = new BufferedReader(new FileReader(archivo))) {
            String linea;
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            while ((linea = reader.readLine()) != null) {
                // Suponiendo que cada línea tiene formato: Placa,DPI,Nombre,Marca,Modelo,Año,Multas,Traspasos
                String[] datos = linea.split(",");
                if (datos.length == 8) {
                    // Crear una nueva fila con las 8 columnas del archivo + la columna Departamento
                    Object[] fila = new Object[9];
                    System.arraycopy(datos, 0, fila, 0, 8); // Copiar Placa, DPI, Nombre, Marca, Modelo, Año, Multas, Traspasos
                    fila[8] = nombreCarpeta; // Establecer Departamento como el nombre de la carpeta seleccionada
                    modelo.addRow(fila);
                } else {
                    System.err.println("Línea inválida en " + archivo.getName() + ": " + linea);
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error al leer el archivo: " + e.getMessage());
        }
    }

